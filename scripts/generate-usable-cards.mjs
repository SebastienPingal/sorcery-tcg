import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const KEYWORD_PATTERNS = [
  ['airborne', /\bairborne\b/i],
  ['burrowing', /\bburrow(?:ing|er|ers)?\b/i],
  ['charge', /\bcharge\b/i],
  ['deathrite', /\bdeathrite\b/i],
  ['disable', /\bdisable(?:d)?\b/i],
  ['genesis', /\bgenesis\b/i],
  ['immobile', /\bimmobile\b/i],
  ['lance', /\blance\b/i],
  ['lethal', /\blethal\b/i],
  ['ranged', /\branged(?:\s+\d+)?\b/i],
  ['spellcaster', /\bspellcaster(?:s)?\b/i],
  ['stealth', /\bstealth\b/i],
  ['submerge', /\bsubmerge(?:d|r|rs|s)?\b/i],
  ['voidwalk', /\bvoidwalk(?:er|ers)?\b/i],
  ['ward', /\bward(?:ed|s)?\b/i],
  ['waterbound', /\bwaterbound\b/i],
  ['flooded', /\bflood(?:ed|ing|s)?\b/i],
];

function parseRealCardsSource(tsSource) {
  const exportMarker = 'export const REAL_CARDS: Card[] =';
  const markerIndex = tsSource.indexOf(exportMarker);
  if (markerIndex === -1) {
    throw new Error('REAL_CARDS export not found in src/data/realCards.ts');
  }
  const arrayStart = tsSource.indexOf('[', markerIndex);
  const arrayEnd = tsSource.lastIndexOf('];');
  if (arrayStart === -1 || arrayEnd === -1) {
    throw new Error('Could not locate REAL_CARDS array bounds');
  }
  const arrayCode = tsSource.slice(arrayStart, arrayEnd + 1);
  return Function(`"use strict"; return (${arrayCode});`)();
}

function extractKeywordsFromCard(card) {
  const existing = Array.isArray(card.keywords) ? card.keywords : [];
  const abilityText = Array.isArray(card.abilities)
    ? card.abilities.map((ability) => ability?.description ?? '').join('\n')
    : '';
  const text = `${card.rulesText ?? ''}\n${abilityText}`;
  const merged = new Set(existing);
  for (const [keyword, pattern] of KEYWORD_PATTERNS) {
    if (pattern.test(text)) merged.add(keyword);
  }
  return Array.from(merged);
}

function inferCasterChoicePolicy(card) {
  const abilityText = Array.isArray(card.abilities)
    ? card.abilities.map((ability) => ability?.description ?? '').join('\n')
    : '';
  const text = `${card.rulesText ?? ''}\n${abilityText}`.toLowerCase();
  if (/\bmay be cast by an allied mortal\b/i.test(text)) return 'custom';
  if (card.type === 'minion') return 'auto';
  if (text.includes('projectile')) return 'require_choice';
  return 'auto';
}

function inferCasterEligibility(card) {
  const abilityText = Array.isArray(card.abilities)
    ? card.abilities.map((ability) => ability?.description ?? '').join('\n')
    : '';
  const text = `${card.rulesText ?? ''}\n${abilityText}`.toLowerCase();
  if (/\bmay be cast by an allied mortal\b/i.test(text)) {
    return { all: [{ predicate: 'has_subtype', params: { subtype: 'Mortal' } }] };
  }
  return { all: ['spellcaster'] };
}

function buildUsableCard(card, overridesById) {
  const baseWithKeywords = Array.isArray(card.keywords)
    ? { ...card, keywords: extractKeywordsFromCard(card) }
    : card;
  const base = {
    ...baseWithKeywords,
    casterChoicePolicy: inferCasterChoicePolicy(baseWithKeywords),
    casterEligibility: inferCasterEligibility(baseWithKeywords),
  };
  const override = overridesById[card.id];
  if (!override || typeof override !== 'object') return base;
  return { ...base, ...override };
}

function main() {
  const root = process.cwd();
  const realCardsPath = resolve(root, 'src/data/realCards.ts');
  const overridesPath = resolve(root, 'src/data/usableCardOverrides.json');
  const outputPath = resolve(root, 'src/data/usableCards.ts');

  const realCardsSource = readFileSync(realCardsPath, 'utf8');
  const realCards = parseRealCardsSource(realCardsSource);
  const overridesById = JSON.parse(readFileSync(overridesPath, 'utf8'));
  const usableCards = realCards.map((card) => buildUsableCard(card, overridesById));

  const output = `import type { Card } from '../types';

// Auto-generated by scripts/generate-usable-cards.mjs
// 1) Source: src/data/realCards.ts
// 2) Manual edge-case patches: src/data/usableCardOverrides.json
export const USABLE_CARDS: Card[] = ${JSON.stringify(usableCards, null, 2)};
`;
  writeFileSync(outputPath, output, 'utf8');
  console.log(`Generated ${usableCards.length} cards at src/data/usableCards.ts`);
}

main();
